  CREATE OR REPLACE EDITIONABLE TRIGGER "ADMIN_USERS_T_DELETE" 
before
delete on "ADMIN_USERS"
for each row
begin
    Update EMPLOYEES e
    Set WORKER_TITLE_ID = ''
    WHERE lower(e.USER_NAME) = lower(:OLD.USER_NAME);
    
    -- Update MANAGER Role
    for rec in (select emp.id,emp.WORKER_TITLE_ID 
                from employees emp join 
                (select MANAGER_ID from employees where MANAGER_ID is not null group by MANAGER_ID) mt 
                on emp.EMPLOYEE_CODE = mt.MANAGER_ID
                Where lower(emp.USER_NAME) = lower(:OLD.USER_NAME))
    loop
        if rec.WORKER_TITLE_ID is null then
            update employees set WORKER_TITLE_ID = 'MANAGER' where rec.id = id;
        end if;
    end loop;
end;
/
